# control statement Ep.03
# คำสั่ง break และ continue ใน Loop
# break ใน Loop ทำงานเมื่อใด หยุดทำซ้ำทันที
# continue ใน Loop ทำงานเมื่อใด ไปรอบต่อไปทันที
# while
i = 1
while i <= 5 :
    print(f'Timmy{i}')
    i = i + 1
print("END")
print('-------------------------------------')

#for-in
for x in range(5) :
     print(f'Timmy{x + 1}')
print("END")
print('-------------------------------------')
#กำหนดค่าตอนเริ่ม
for x in range(1,6) :
     print(f'Timmy{x}')
print("END")
print('-------------------------------------')

