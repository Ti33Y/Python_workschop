# while
i = 1
while i <= 5 :
    print(f'Timmy{i}')
    i = i + 1
print("END")
print('-------------------------------------')

#for-in
for x in range(5) :
     print(f'Timmy{x + 1}')
print("END")
print('-------------------------------------')

for x in range(1,6) :
     print(f'Timmy{x}')
print("END")
print('-------------------------------------')